/*
Sliding Window Problems :
------------------------

Sliding window technique is useful for solving problems in array or string, especially it is considered as
a technique that could reduce the time complexity from O(n^2) to O(n).

*/

/*
Type-I : Fixed Window length k 

The length of the window is fixed and it asks you to find
something in the window such as the maximum sum of all
windows, the maximum or median number of each
window. Usually we need kind of variables to maintain the
state of the window, some are as simple as a integer or it
could be as complicated as some advanced data structures
such as list, queue or deque

*/


/*
Type- II : Two Pointers + Criteria

The window size is not fixed, usually it asks you to find the
subarray that meets the criteria .

For example, given an array of integers, Find the number
of subarrays whose sum is equal to a target value.

*/